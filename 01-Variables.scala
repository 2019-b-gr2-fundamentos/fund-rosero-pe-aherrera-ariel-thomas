/* Primitivas (Tipos de Variables)
byte          ->Numero entero de 8 bits                     Ej: Entre -128 y 127
Int           ->Numero entero                               Ej: 6000
Float         ->Numero decimal                              Ej: 98.098
double        ->Numero decimal +++                          Ej: 128.999
char          ->Caracter                                    Ej: N
string        ->Cadena de Caracteres                        Ej: "Deberes"
null          ->Vacio                                       Ej: null
boolean       ->Verdadero/falso                             Ej: True o False

var myVar: string = "test"   ->Variable Mutable
val myval: string = "test"   ->Variable Inmutable (Constante)

 */